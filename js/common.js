import mFn from "./my_function.js";



/****************************************************
     [ 검색버튼 클릭시 검색창 열고 닫기 ]
******************************************************/

// 1. 대상선정
// 검색버튼
const icon = mFn.qs(".icon");
// 검색창
const search = mFn.qs(".search");

// 2. 함수실행: 검색버튼 클릭시 검색창 열리고 닫히기
icon.onclick = () => {
  search.classList.toggle("on");
};

